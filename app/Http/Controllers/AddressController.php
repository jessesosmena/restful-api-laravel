     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'addressline'=>'required',
            'city'=>'required',
            'zip'=>'required',
            'phone'=>'required'
        ]);

        Auth::user()->address()->create($request->all()); // Insert user_id of logged in user to addresses table

        return redirect()->route('checkout.payment'); // Redirected to CheckoutController@payment
    }

